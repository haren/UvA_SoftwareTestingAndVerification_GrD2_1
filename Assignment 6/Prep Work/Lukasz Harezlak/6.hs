exM_fast :: Integer -> Integer -> Integer -> Integer
exM_fast b e m = exM_fast' 1 0 b e m where 
   exM_fast' c e' b e m
   	| e' >= e 		 = c
    | otherwise      = exM_fast' (mod (b*c) m) (e'+1) b e m

